using System;
using System.Collections.Generic;
using System.Text;

namespace BlueC_OperatorParty
{
    class Calculator
    {
        public int Min(int getal1, int getal2)
        {
            return getal1 - getal2;
        }
        public int Plus(int getal1, int getal2)
        {
            return getal1 + getal2;
        }
        public int Keer(int getal1, int getal2)
        {
            return getal1 * getal2;
        }
        public int Gedeeld(int getal1, int getal2)
        {
            return getal1 / getal2;
        }
    }
}
